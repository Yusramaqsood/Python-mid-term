print("calculator")
num1=int(input("enter your first number:"))
num2=int(input("enter your second number:"))
print ("press + for addition /n press - for subtraction /n press * for multiplication press / for division")
choice=input("enter your choice for  + - * /")
if choice == "+":
    print(num1+num2)

elif choice == "-":
    print(num1-num2)

elif choice == "*":
    print(num1*num2)

elif choice == "/":
    print(num1/num2)
    



    
           
